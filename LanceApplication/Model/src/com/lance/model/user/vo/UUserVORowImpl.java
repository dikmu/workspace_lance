    public void updateSearchIndex() {
        StringBuffer locSbA = new StringBuffer();
        findLocationName(this.getLocationARegion(), this.getLocationACountry(), this.getLocationAProvince(),
                         this.getLocationACity(), locSbA);
        if (locSbA.length() > 0) {
            this.setLocationAIndex(LocationUtil.formatLocationName(locSbA.substring(0,
                                                                                    locSbA.length() - 1).toString()));
        } else {
            this.setLocationAIndex("");
        }
        StringBuffer locSbB = new StringBuffer();
        findLocationName(this.getLocationBRegion(), this.getLocationBCountry(), this.getLocationBProvince(),
                         this.getLocationBCity(), locSbB);
        if (locSbB.length() > 0) {
            this.setLocationBIndex(LocationUtil.formatLocationName(locSbB.substring(0,
                                                                                    locSbB.length() - 1).toString()));
        } else {
            this.setLocationBIndex("");
        }

        this.setIndexAll(new ClobDomain(this.getKeywords() + ";" + this.getTagline() + ";" +
                                        this.getServiceDescription() + ";" + this.getOverview() + ";" +
                                        this.getLocationAIndex() + ";" + this.getLocationBIndex() + ";"+this.getIndexSkill()+";"));
    }

